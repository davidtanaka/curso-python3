# Variáveis: Definição de variáveis e operações com elas

a = 10  # A variável 'a' é inicializada com o valor inteiro 10.
b = 5.2  # A variável 'b' é inicializada com o valor decimal (float) 5.2.

print(a + b)  # Imprime o resultado da soma entre 'a' e 'b'. O valor será 15.2, pois 10 (inteiro) + 5.2 (float) = 15.2.

a = 'Agora eu sou uma string!'  # A variável 'a' é reatribuída com uma string. Agora 'a' não é mais um número, mas uma string.
print(a)  # Imprime o conteúdo da variável 'a', que agora é a string 'Agora eu sou uma string!'.
print(b)  # Imprime o valor da variável 'b', que continua sendo o número 5.2 (não foi alterado).
