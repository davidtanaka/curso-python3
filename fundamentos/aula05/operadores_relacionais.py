# Exemplos de operadores relacionais: >, >=, <, <=, !=, ==
# Comparações entre números inteiros e strings
print(3 > 4)  # Maior que
print(4 >= 3) # Maior ou igual
print(1 < 2)  # Menor que
print(3 <= 1) # Menor ou igual
print(3 != 2) # Diferente
print(4==4)  # Igual
print(4=='4') # Igualdade entre tipos diferentes
